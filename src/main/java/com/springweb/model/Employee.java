package com.springweb.model;

public class Employee {
    private String name;
    private String password;
  
    // Getters and Setters
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

   

    public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		password = password;
	}

	
}